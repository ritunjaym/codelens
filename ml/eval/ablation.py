"""
LoRA rank ablation study for CodeLens reranker.

Loads CodeBERT, applies LoRA at r=4,8,16,32, counts trainable parameters,
measures p50 inference latency over 200 forward passes, and outputs ablation_results.md.

Usage:
    python -m ml.eval.ablation
"""
from __future__ import annotations

import json
import statistics
import time
from pathlib import Path

import structlog

logger = structlog.get_logger()

RANKS = [4, 8, 16, 32]
N_WARMUP = 20
N_MEASURE = 200
BASE_MODEL = "microsoft/codebert-base"
DUMMY_TEXT = "def foo(x): return x + 1 [SEP] def foo(x): return x * 2"


def count_params(model) -> tuple[int, int]:
    """Return (trainable_params, total_params)."""
    trainable = sum(p.numel() for p in model.parameters() if p.requires_grad)
    total = sum(p.numel() for p in model.parameters())
    return trainable, total


def measure_latency(model, tokenizer, device: str, n_warmup: int, n_measure: int) -> dict:
    """Run forward passes and return latency stats in ms."""
    import torch

    enc = tokenizer(
        DUMMY_TEXT,
        return_tensors="pt",
        truncation=True,
        max_length=128,
        padding="max_length",
    )
    input_ids = enc["input_ids"].to(device)
    attention_mask = enc["attention_mask"].to(device)

    model.eval()
    with torch.no_grad():
        for _ in range(n_warmup):
            model(input_ids=input_ids, attention_mask=attention_mask)

    latencies = []
    with torch.no_grad():
        for _ in range(n_measure):
            t0 = time.perf_counter()
            model(input_ids=input_ids, attention_mask=attention_mask)
            latencies.append((time.perf_counter() - t0) * 1000)

    latencies.sort()
    p50 = statistics.median(latencies)
    p95 = latencies[int(0.95 * len(latencies))]
    p99 = latencies[int(0.99 * len(latencies))]
    return {"p50_ms": round(p50, 2), "p95_ms": round(p95, 2), "p99_ms": round(p99, 2)}


def run_ablation() -> list[dict]:
    import torch
    from transformers import AutoModelForSequenceClassification, AutoTokenizer

    try:
        from peft import get_peft_model, LoraConfig, TaskType
    except ImportError:
        logger.warning("peft not installed — install with: pip install peft")
        raise

    device = "cuda" if torch.cuda.is_available() else "cpu"
    logger.info("running lora rank ablation", device=device, base_model=BASE_MODEL)

    tokenizer = AutoTokenizer.from_pretrained(BASE_MODEL, cache_dir="/tmp/hf-cache")

    results = []
    for r in RANKS:
        logger.info("evaluating lora rank", r=r)
        base_model = AutoModelForSequenceClassification.from_pretrained(
            BASE_MODEL, num_labels=1, cache_dir="/tmp/hf-cache"
        )

        lora_cfg = LoraConfig(
            task_type=TaskType.SEQ_CLS,
            r=r,
            lora_alpha=r * 2,
            lora_dropout=0.1,
            target_modules=["query", "value"],
        )
        model = get_peft_model(base_model, lora_cfg)
        model.to(device)

        trainable, total = count_params(model)
        reduction_pct = round((1 - trainable / total) * 100, 1)

        latency = measure_latency(model, tokenizer, device, N_WARMUP, N_MEASURE)

        row = {
            "r": r,
            "alpha": r * 2,
            "trainable_params": trainable,
            "total_params": total,
            "reduction_pct": reduction_pct,
            **latency,
        }
        results.append(row)
        logger.info(
            "rank ablation result",
            r=r,
            trainable=trainable,
            reduction_pct=reduction_pct,
            p50_ms=latency["p50_ms"],
        )

        del model, base_model

    return results


def write_markdown(results: list[dict], output_path: Path) -> None:
    lines = [
        "# LoRA Rank Ablation Study",
        "",
        "Base model: `microsoft/codebert-base` (125M parameters)",
        f"Latency measured over {N_MEASURE} forward passes (batch=1, seq_len=128) after {N_WARMUP} warmup passes.",
        "",
        "| r | α | Trainable Params | % Reduction | p50 ms | p95 ms | p99 ms |",
        "|---|---|-----------------|-------------|--------|--------|--------|",
    ]
    for row in results:
        lines.append(
            f"| {row['r']} | {row['alpha']} "
            f"| {row['trainable_params']:,} "
            f"| {row['reduction_pct']}% "
            f"| {row['p50_ms']} "
            f"| {row['p95_ms']} "
            f"| {row['p99_ms']} |"
        )
    lines += [
        "",
        "**Key takeaways:**",
        "- Higher r → more trainable parameters, marginally slower inference",
        "- r=16 (used in production) balances expressivity and efficiency",
        "- All ranks reduce trainable parameters by >98% vs full fine-tuning",
        "",
        f"*Generated by `python -m ml.eval.ablation`*",
    ]
    output_path.write_text("\n".join(lines) + "\n")
    logger.info("ablation results written", path=str(output_path))


def main() -> None:
    output_path = Path("ml/eval/ablation_results.md")
    results = run_ablation()
    write_markdown(results, output_path)

    # Also write JSON for downstream use
    json_path = output_path.with_suffix(".json")
    json_path.write_text(json.dumps(results, indent=2))
    logger.info("ablation complete", md=str(output_path), json=str(json_path))


if __name__ == "__main__":
    main()
